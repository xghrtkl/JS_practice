var sort = function(input){
    var arrays = input
    return arrays.sort()
};

var result1 = sort([2, 3, 1]);
var result2 = sort([1, 3, 2]);
var result3 = sort([2, 1, 3]);
var result4 = sort([3, 1, 2]);

document.write('result1 =     ' + result1);
document.write('<br>result2 = ' + result2);
